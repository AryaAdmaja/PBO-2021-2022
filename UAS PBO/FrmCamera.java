    public void tampilkanData() {
        String[] kolom = {"ID", "Kategori", "Merk", "Type", "tahunRilis"};
        ArrayList<Camera> list = new Camera().getAll();
        Object rowData[] = new Object[5];

        tblCamera.setModel(new DefaultTableModel(new Object[][]{}, kolom));

        for (int i = 0; i < list.size(); i++) {
            rowData[0] = list.get(i).getIdCamera();
            rowData[1] = list.get(i).getKategori().getNama();
            rowData[2] = list.get(i).getMerk();
            rowData[3] = list.get(i).getType();
            rowData[4] = list.get(i).getTahunRilis();

            ((DefaultTableModel) tblCamera.getModel()).addRow(rowData);
        }
    }

    public void cari(String keyword) {
        String[] kolom = {"ID", "Kategori", "Merk", "Type", "tahunRilis"};
        ArrayList<Camera> list = new Camera().search(keyword);
        Object rowData[] = new Object[5];

        tblCamera.setModel(new DefaultTableModel(new Object[][]{}, kolom));

        for (Camera camera : list) {
            rowData[0] = camera.getIdCamera();
            rowData[1] = camera.getKategori().getNama();
            rowData[2] = camera.getMerk();
            rowData[3] = camera.getType();
            rowData[4] = camera.getTahunRilis();

            ((DefaultTableModel) tblCamera.getModel()).addRow(rowData);
        }
    }

    public void tampilkanCmbKategori() {
        cmbKategori.setModel(new DefaultComboBoxModel(new Kategori().getAll().toArray()));
    }

