    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        Camera camera = new Camera();
        camera.setIdCamera(Integer.parseInt(txtIdCamera.getText()));
        camera.setKategori((Kategori) cmbKategori.getSelectedItem());
        camera.setMerk(txtMerk.getText());
        camera.setType(txtType.getText());
        camera.setTahunRilis(Integer.parseInt(txtTahunRilis.getText()));
        camera.save();
        txtIdCamera.setText(Integer.toString(camera.getIdCamera()));
        tampilkanData();
    }//GEN-LAST:event_btnSimpanActionPerformed

