    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        DefaultTableModel model = (DefaultTableModel) tblCamera.getModel();
        int row = tblCamera.getSelectedRow();

        Camera camera = new Camera().getById(Integer.parseInt(model.getValueAt(row, 0).toString()));
        camera.delete();
        kosongkanForm();
        tampilkanData();
    }//GEN-LAST:event_btnHapusActionPerformed

    private void tblCameraMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCameraMouseClicked
        DefaultTableModel model = (DefaultTableModel) tblCamera.getModel();
        int row = tblCamera.getSelectedRow();
        Camera camera = new Camera();

        camera = camera.getById(Integer.parseInt(model.getValueAt(row, 0).toString()));
        txtIdCamera.setText(String.valueOf(camera.getIdCamera()));
        cmbKategori.getModel().setSelectedItem(camera.getKategori());
        txtMerk.setText(camera.getMerk());
        txtType.setText(camera.getType());
        txtTahunRilis.setText(String.valueOf(camera.getTahunRilis()));
    }//GEN-LAST:event_tblCameraMouseClicked

